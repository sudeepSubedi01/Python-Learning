name = "hari bahadur khadka"

print(len(name)) #length of string
print(name.find("u")) #index of u
print(name.find('a')) #index of first a
print(name.capitalize()) #first letter is capitalized but not space and numbers
print(name.upper()) #whole to uppercase
print(name.lower()) #whole to lowercase
print(name.isdigit()) #returns true if content of the variable is whole numbers
print(name.isalpha()) #returns true if content of the variable is whole alphabets
print(name.count("a")) #counts the number of a
print(name.replace("a","x"))
print(name.split(' '))
print(name*3)